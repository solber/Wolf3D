#include <unistd.h>

void	ft_print_bits(unsigned char c)
{
	int z;

	z = 128;
	while (z > 0)
	{
		if (c & z)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		z >>= 1;
	}
}
