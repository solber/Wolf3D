/*
** On parcour le tableau pour renvoyer ca valeur 
*/

int				map_get(t_map *map, int x, int y)
{
	int		pos;

	if (map != 0 && x < MAP_WIDTH)
	{
		pos = x + (y * MAP_WIDTH);
		if (pos >= 0 && pos < (MAP_WIDTH * MAP_HEIGHT))
		{
			return (map->data[pos]);
		}
	}
	return (0xffffff);
}

/*
** on renvoi la couleur en fonction de l'orientation
*/

int				map_color(int side)
{
	if (side == 1)
		return (0xFF0000);
	if (side == 2)
		return (0x002FFF);
	if (side == 3)
		return (0x0DFF00);
	return (0xFFEE00);
}
